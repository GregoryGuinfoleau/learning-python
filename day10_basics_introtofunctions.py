# Functions are a convenient way to group our code into 
# reusable blocks. A function contains a sequence of steps 
# that can be performed repeatedly throughout a program without
# having to repeat the process of writing the same code again

# Pour écrire une fonction :

# def nom_fonction():
#   début du code

# Exemple :
def welcome():
    print("Bienvenue!")

# Ceci n'affichera pas Bienvenue, il faut "appeler" la fonction pour cela.
# tout simplement en écrivant nom_fonction() après l'avoir défini bine sûr !

welcome()


